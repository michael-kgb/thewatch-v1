function registerTimexRaffle1(){
    $("#submitRegistrant").attr("disabled", true);
        $("#submitRegistrant").html('Waiting');



          var nama = $('input[name=nama1]').val();
          var email = $('input[name=mail1]').val();
          var hp = $('input[name=hp1]').val();
          var ktp = $('input[name=ktp1]').val();

          nama = $.trim(nama);
          email = $.trim(email);
          hp = $.trim(hp);
          ktp = $.trim(ktp);

          console.log("Nama:"+nama);
          console.log("Email:"+email);
          console.log("Hp:"+hp);
          console.log("KTP:"+ktp);

          if(nama!=""&&
          email!=""&&
          hp!=""&&
          ktp!=""&&
          nama.length>=3&&
          email.length>=4&&
          hp.length>=10&&
          ktp.length==16&&
          isEmail(email)){

          $.ajax({
            type: "POST",
            url: "https://thewatch.co/go-api/collaborate-user/sign-up/",
            data: { 
              name: nama,
              key: ktp,
              email: email,
              phone: hp,
              regdate: tanggal(),
              event: 'timexraffle'
             },
            dataType: "text",
            success: function(resultData){
             
                hasil = JSON.parse(resultData)
              
              if(hasil['message']=="Data created successfully."){
                alert("Sukses");
              }else{
                alert(hasil['message']);
              }
              $("#submitRegistrant").attr("disabled", false);
              $("#submitRegistrant").html('Submit');
            },
            error: function (xhr, ajaxOptions, thrownError) {
               alert("Error, Submit dibatalkan");
               $("#submitRegistrant").attr("disabled", false);
               $("#submitRegistrant").html('Submit');
               console.log(e);
            }
          });

        }else{
          alert("Sesuaikan data yang anda masukan");
          $("#submitRegistrant").attr("disabled", false);
          $("#submitRegistrant").html('Submit');
        }
}


